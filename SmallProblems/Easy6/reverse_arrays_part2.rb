'''
- create a new array for the reversed order
- iterate through the original array and prepend the values to the new array
- return the new array 
'''

def reverse(list)
  reversed_list = []
  list.each {|i| reversed_list.prepend(i)}
  reversed_list 
end

p reverse([1,2,3,4]) == [4,3,2,1]          # => true
p reverse(%w(a b e d c)) == %w(c d e b a)  # => true
p reverse(['abc']) == ['abc']              # => true
p reverse([]) == []                        # => true

list = [1, 3, 2]                      # => [1, 3, 2]
new_list = reverse(list)              # => [2, 3, 1]
list.object_id != new_list.object_id  # => true
list == [1, 3, 2]                     # => true
new_list == [2, 3, 1]                 # => true